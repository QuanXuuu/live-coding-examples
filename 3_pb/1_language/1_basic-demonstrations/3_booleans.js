// Equal to operator: == (Equality)

// True
9 == 9;

// False
4 == 8;

// False
3 == "5";

// True:
4 == "4";

// Equal to by type and value type: === (Identity)

// False:
4 === "4"


// Not equal operator: !==

// True:
1 !== 2;
5 !== "5";

// Greater than operator: >

// True:
5 > 4;

// Lesser than operator: <

// True:
3 < 6;

// Greater than or equal operator: >=
// Lesser than or equal to operator: <=

